    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
         if (manufactureField.getText().length() == 0 ||
                    countryField.getText().length() == 0 ||
                    modelField.getText().length() == 0||
                    colorsField.getText().length() == 0||
                    Checks.isNumber(sizeField.getText())==false) {
                JOptionPane.showMessageDialog(null, "Пожалуйста,проверье корректность введённых данных","Внимание",JOptionPane.WARNING_MESSAGE);
            } else {
             
                Sneakers newSneakers=new Sneakers();
                newSneakers.setColors(colorsField.getText());
                newSneakers.setCountry(countryField.getText());
                newSneakers.setManufacture(manufactureField.getText());
                newSneakers.setModel(modelField.getText());
                newSneakers.setSize(Integer.parseInt(sizeField.getText()));
                
                SneakersDAO dao=new SneakersDAO();
                if(dao.add(newSneakers)) {
                    JOptionPane.showMessageDialog(null, "Информация добавлена!!!", "Добавление", JOptionPane.INFORMATION_MESSAGE);
                    update_table();
                }
                else
                    JOptionPane.showMessageDialog(null, "ERROR 412", "Ошибка!", JOptionPane.ERROR_MESSAGE);
                
                update_table();

            }
         
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if(modelField.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Товар не выбран", "Ошибка!", JOptionPane.WARNING_MESSAGE);
        }
        else{
            
            SneakersDAO dao=new SneakersDAO();
            int id=dao.getID(new Sneakers(0,manufactureField.getText(),countryField.getText(),modelField.getText(),Integer.parseInt(sizeField.getText()),colorsField.getText()));
            if (id!=-1) {
                dao.delete(id);
                JOptionPane.showMessageDialog(null, "Информация о сотруднике удалена", "Удаление", JOptionPane.INFORMATION_MESSAGE);
                update_table();
            }
            else
                JOptionPane.showMessageDialog(null, "Ошибка!", "Ошибка!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

