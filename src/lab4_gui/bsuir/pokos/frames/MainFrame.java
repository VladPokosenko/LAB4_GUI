    private void show_sneakers_in_table() 
    {
        
        ArrayList<Sneakers> result=null;
        SneakersDAO dao=new SneakersDAO();
        
        result= dao.getALL();
        DefaultTableModel model=(DefaultTableModel) sneakersTable.getModel();
        String[] row;            
            
        for(int i=0;i<result.size();i++)
        {     
            row = new String[5]; 
                
            row[0]=result.get(i).getModel();
            row[1]=result.get(i).getColors();
            row[2]=String.valueOf(result.get(i).getSize());
            row[3]=result.get(i).getManufacture();
            row[4]=result.get(i).getCountry();
            model.addRow(row);
        }
        
    }
    
    private void update_table()
    {
        DefaultTableModel model=(DefaultTableModel) sneakersTable.getModel();
        model.setRowCount(0);
        show_sneakers_in_table();
    }
        
